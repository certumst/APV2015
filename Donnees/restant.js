function add_projs_restantes(){
    for(k=0;k<to_add.length;k++){
        ids = []
        for(j=0;j<to_add[k].liste_videos.length;j++){
            add_video_res(to_add[k].liste_videos[j], to_add[k].category, to_add[k].id+j , proj=to_add[k].id, promo=to_add[k].promo)
            ids.push(to_add[k].id+j)
        }
        to_add[k].liste_videos = ids
        to_add[k]['image'] = "../../Contenu/default_proj.jpg",
        a_proj.push(to_add[k])
    }
}

